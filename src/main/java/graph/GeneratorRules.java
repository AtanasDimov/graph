package graph;

public class GeneratorRules {
    private int locationsCount;
    private int graphPopulation;
    private int populationRate;

    public int getLocationsCount() {
        return locationsCount;
    }

    public void setLocationsCount(int locationsCount) {
        this.locationsCount = locationsCount;
    }

    public int getGraphPopulation() {
        return graphPopulation;
    }

    public void setGraphPopulation(int graphPopulation) {
        this.graphPopulation = graphPopulation;
    }

    public int getPopulationRate() {
        return populationRate;
    }

    public void setPopulationRate(int populationRate) {
        this.populationRate = populationRate;
    }
}
